#include "../push_swap.h"

void	ft_lstclear(t_stack **lst)
{
	t_stack	*tmp;

	if (lst)
	{
		while (*lst)
		{
			tmp = (*lst)->next;
			free(*lst);
			*lst = tmp;
		}
	}
}
